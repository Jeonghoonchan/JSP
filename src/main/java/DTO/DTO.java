package DTO;

public class DTO {

	private String id;
	private String pass;
	private int syslev;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public int getSyslev() {
		return syslev;
	}
	public void setSyslev(int syslev) {
		this.syslev = syslev;
	}
	

}
